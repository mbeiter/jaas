package org.beiter.michael.authn.jaas.common;

/**
 * The JAAS configuration options used by this library.
 */
public enum JaasConfigOptions
        implements ConfigOptions {

    //CHECKSTYLE:OFF
    AUDIT_CLASS("jaas.audit.class"),
    AUDIT_ENABLED("jaas.audit.enabled"),
    MESSAGEQ_CLASS("jaas.messageq.class"),
    MESSAGEQ_ENABLED("jaas.messageq.enabled"),
    VALIDATOR_PASSWORD_CLASS("jaas.validator.password.class"),
    AUTHENTICATOR_PASSWORD_CLASS("jaas.authenticator.password.class");

    private final String name;

    private JaasConfigOptions(final String keyName) {
        this.name = keyName;
    }

    public String getName() { return name; }
    //CHECKSTYLE:ON
}
